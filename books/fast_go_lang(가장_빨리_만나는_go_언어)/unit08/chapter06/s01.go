package main

import "fmt"

func main() {
	var num1_01 uint8 = 3
	var num2_01 uint8 = 2

	fmt.Println(num1_01 + num2_01)
	fmt.Println(num1_01 - num2_01)
	fmt.Println(num1_01 * num2_01)
	fmt.Println(num1_01 / num2_01)
	fmt.Println(num1_01 % num2_01)
	fmt.Println(num1_01 << num2_01)
	fmt.Println(num1_01 >> num2_01)
	fmt.Println(^num1_01)
}
